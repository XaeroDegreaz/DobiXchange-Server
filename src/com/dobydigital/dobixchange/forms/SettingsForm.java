	private void btnSaveActionPerformed( java.awt.event.ActionEvent evt ) {//GEN-FIRST:event_btnSaveActionPerformed
		DobiXchangeServer.configuration.addProperty( "photoshop", txtPhotoshop.getText());
		DobiXchangeServer.configuration.addProperty( "home", txtHome.getText());
		
		try {
			FileOutputStream out = new FileOutputStream( "config-dobixchange.json" );
			out.write( DobiXchangeServer.configuration.toString().getBytes() );
			out.close();
			dispose();
		}catch(Exception e) {
			
		}
	}//GEN-LAST:event_btnSaveActionPerformed

