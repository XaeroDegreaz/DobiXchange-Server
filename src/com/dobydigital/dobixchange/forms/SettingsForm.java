	private void btnBrowsePhotoshopActionPerformed( java.awt.event.ActionEvent evt ) {//GEN-FIRST:event_btnBrowsePhotoshopActionPerformed
		FileFilter filter = new FileFilter() {

			@Override
			public boolean accept( File f ) {
				if(f.getName().equalsIgnoreCase( "photoshop.exe" ) || f.isDirectory() ) {
					return true;
				}
				return false;
			}

			@Override
			public String getDescription() {
				return null;
			}
		};
		
		JFileChooser chooser = new JFileChooser( "C:\\Program Files\\" );
		chooser.setFileFilter( filter );
		
		int choice = chooser.showDialog( this, "Select");
		
		if(choice == JFileChooser.APPROVE_OPTION) {
			txtPhotoshop.setText( chooser.getSelectedFile().getAbsolutePath() );
		}
	}//GEN-LAST:event_btnBrowsePhotoshopActionPerformed

